let currentLanguage = localStorage.getItem('sql-agent-language') || 'zh';

function getLanguageMessages(lang = currentLanguage) {
    return I18N_MESSAGES[lang] || I18N_MESSAGES.zh;
}

function t(key, vars = {}, fallback = '') {
    let value = getLanguageMessages()[key] || fallback || key;
    Object.entries(vars || {}).forEach(([name, tokenValue]) => {
        value = value.replaceAll(`{${name}}`, tokenValue);
    });
    return value;
}

function applyBinding(binding) {
    if (binding.target === 'documentTitle') {
        document.title = t(binding.key);
        return;
    }

    const elements = binding.all
        ? Array.from(document.querySelectorAll(binding.selector))
        : [document.querySelector(binding.selector)].filter(Boolean);
    if (!elements.length) return;

    const element = binding.all && typeof binding.pick === 'number'
        ? elements[binding.pick]
        : elements[0];
    if (!element) return;

    const value = t(binding.key);
    if (binding.target === 'lang') {
        document.documentElement.lang = currentLanguage === 'en' ? 'en' : 'zh-CN';
        return;
    }
    if (binding.target === 'lastChildText') {
        const textNode = Array.from(element.childNodes)
            .find(node => node.nodeType === Node.TEXT_NODE && node.textContent.trim());
        if (textNode) {
            textNode.textContent = ` ${value}`;
        } else {
            element.append(document.createTextNode(` ${value}`));
        }
        return;
    }
    if (binding.target === 'buttonText') {
        const textNode = Array.from(element.childNodes).find(node => node.nodeType === Node.TEXT_NODE && node.textContent.trim());
        if (textNode) {
            textNode.textContent = ` ${value}`;
        }
        return;
    }
    if (binding.target === 'modelLabel') {
        const textNode = Array.from(element.childNodes).find(node => node.nodeType === Node.TEXT_NODE && node.textContent.trim());
        if (textNode) {
            textNode.textContent = ` ${value}`;
        }
        return;
    }
    if (binding.target === 'parentText') {
        element.parentElement.lastChild.textContent = value;
        return;
    }
    if (binding.target === 'noop') {
        return;
    }
    if (binding.attr) {
        element.setAttribute(binding.attr, value);
        return;
    }
    element.textContent = value;
}

function applyI18n() {
    I18N_STATIC_BINDINGS.forEach(applyBinding);
    document.body.dataset.language = currentLanguage;
}

function setLanguage(lang) {
    currentLanguage = lang === 'en' ? 'en' : 'zh';
    localStorage.setItem('sql-agent-language', currentLanguage);
    applyI18n();
    window.dispatchEvent(new CustomEvent('sqlagent:languagechange', { detail: { language: currentLanguage } }));
}

function toggleLanguage() {
    setLanguage(currentLanguage === 'zh' ? 'en' : 'zh');
}

window.t = t;
window.setLanguage = setLanguage;
window.toggleLanguage = toggleLanguage;
window.getCurrentLanguage = () => currentLanguage;

document.addEventListener('DOMContentLoaded', () => {
    applyI18n();
});

